// 打印出所有的"水仙花數"。所謂"水仙花數"是指一個三位數，其各位數字立方和等於該數本身。
// 例如：153是一個"水仙花數"，因為$153=1^3+5^3+3^3$，或370也是水仙花數，因為$370=3^3+7^3+0^3$。
// PPDI(); // 153, 370, 371, 407


function PPDI(){
     for(let i = 100 ;  i<=999;i++ ){
        if(Math.pow(i%10,3) + Math.pow(Math.floor(i/10)%10,3) + Math.pow(Math.floor(i/100)%10,3) == i){
            console.log(i);
        }
     }
}


PPDI();